
#ifndef __CN_BASE_WRAPPER_HPP__
#define __CN_BASE_WRAPPER_HPP__

/*---------------------------------------------------------------------------*/

namespace Framework {
namespace Core {
namespace Connector {

/*---------------------------------------------------------------------------*/

template< typename _TBase >
class BaseWrapper
	:	public _TBase
{

/*---------------------------------------------------------------------------*/

public:

/*---------------------------------------------------------------------------*/

	BaseWrapper()
		:	m_counter( 0 )
	{}

	virtual ~BaseWrapper() {}

/*---------------------------------------------------------------------------*/

	virtual void addRef()
	{
		++m_counter;
	}

	virtual void releaseRef()
	{
		if ( --m_counter == 0 )
			delete this;
	}

/*---------------------------------------------------------------------------*/

private:

/*---------------------------------------------------------------------------*/

	unsigned int m_counter;

/*---------------------------------------------------------------------------*/

};

/*---------------------------------------------------------------------------*/

} // namespace Connector
} // namespace Core
} // namespace Framework

/*---------------------------------------------------------------------------*/

#endif // __CN_BASE_WRAPPER_HPP__
