
#ifndef __IT_IITERATOR_HPP__
#define __IT_IITERATOR_HPP__


/*---------------------------------------------------------------------------*/

namespace Tools {

/*---------------------------------------------------------------------------*/

template< typename _TReturnType >
struct IIterator
{

/*---------------------------------------------------------------------------*/

	typedef _TReturnType ReturnType;

/*---------------------------------------------------------------------------*/

	virtual bool isValid() const = 0;

	virtual ReturnType current() const = 0;

	virtual void next() = 0;

/*---------------------------------------------------------------------------*/

};

/*---------------------------------------------------------------------------*/

} // namespace Tools

/*---------------------------------------------------------------------------*/

#endif // __IT_IITERATOR_HPP__
